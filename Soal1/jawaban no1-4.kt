//1) -Variabel
//    Variabel adalah tempat penyimpanan data yang memiliki nama dan tipe tertentu. Di Kotlin, ada dua jenis variabel.
//        - val : Untuk variabel yang nilainya tidak dapat diubah (immutable).
//        - var : Untuk variabel yang nilainya dapat diubah (mutable).
//   -Tipe Data
//    Tipe data mendefinisikan jenis nilai yang dapat disimpan dalam variabel.Contoh tipe data.
//    1. Angka (Numerik):
//        - Byte, Short, Int, Long (bilangan bulat)
//        - Float, Double (bilangan desimal)
//    2. Karakter (Character):
//        - Char
//    3. Boolean:
//        - Boolean (true/false)
//    4. String:
//        - String (teks)
//    5. Array:
//        - Array (kumpulan data dengan tipe yang sama)
//
//2) percadangan dalam bahasa pemrograman kotlin
//   - if dalam Kotlin adalah sebuah ekspresi, artinya dapat mengembalikan nilai dan digunakan dalam konteks lain seperti
//     assignment atau return statement.
//   - when adalah percabangan yang lebih fleksibel dibandingkan if. Ini sering digunakan sebagai pengganti switch
//     case di bahasa lain.
//   Perbedaan :
//    -if : Sederhana dan langsung. Lebih cocok untuk kondisi dengan dua atau tiga kemungkinan hasil.
//    -when : Lebih cocok untuk kasus dengan banyak kemungkinan nilai atau kondisi yang berbeda.
//
//3) Function
//    fungsi (function) adalah blok kode yang dirancang untuk melakukan tugas tertentu.ungsi memungkinkan Anda untuk
//    membagi program menjadi bagian-bagian yang lebih kecil, lebih mudah dipahami, dan lebih terorganisir.
//
//4) Function generic dalam kotlin
//   function generics memungkinkan Anda untuk menulis fungsi yang dapat beroperasi pada berbagai tipe data tanpa harus
//   menulis kode yang sama untuk setiap tipe. Dengan menggunakan generik('<>'), Anda dapat membuat fungsi yang lebih fleksibel
//   dan dapat digunakan ulang, mengurangi duplikasi kode dan meningkatkan keterbacaan.
//    Perbedaan:
//    - Fungsi biasa : Bekerja dengan tipe data tertentu yang telah ditentukan.
//    - Fungsi generic: Bekerja dengan berbagai tipe data menggunakan satu definisi fungsi yang sama. text